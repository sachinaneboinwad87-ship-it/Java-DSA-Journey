package javadsa.basics;

public class DataTypes {
    static void main() {


        // Java Data Types

        // Primitive Data Types


        // byte: 1 byte (-128 to 127)
        byte age = 22;

        // short: 2 bytes
        short year = 2026;

        // int: 4 bytes (most commonly used for whole numbers)
        int salary = 50000;

        // long: 8 bytes (use L at the end)
        long population = 1400000000L;

        // float: 4 bytes (use F at the end)
        float percentage = 89.5F;

        // double: 8 bytes (default for decimal numbers)
        double pi = 3.1415926535;

        // char: 2 bytes (stores a single character)
        char grade = 'A';

        // boolean: stores only true or false
        boolean isPassed = true;


        // Non-Primitive Data Type


        // String stores a sequence of characters
        String name = "Sachin";


        // Printing Values


        System.out.println("Byte Value: " + age);
        System.out.println("Short Value: " + year);
        System.out.println("Int Value: " + salary);
        System.out.println("Long Value: " + population);
        System.out.println("Float Value: " + percentage);
        System.out.println("Double Value: " + pi);
        System.out.println("Char Value: " + grade);
        System.out.println("Boolean Value: " + isPassed);
        System.out.println("String Value: " + name);
    }
}
